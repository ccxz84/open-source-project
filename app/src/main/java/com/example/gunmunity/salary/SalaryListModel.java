package com.example.gunmunity.salary;

public class SalaryListModel {

    String phase_text;
    String curclass;
    String payment_date;
    String payment_money;
    String payment_allmoney;


    public String getPhase_text() {
        return phase_text;
    }

    public String getCurclass() {
        return curclass;
    }

    public String getPayment_date() {
        return payment_date;
    }

    public String getPayment_money() {
        return payment_money;
    }

    public String getPayment_allmoney() {
        return payment_allmoney;
    }


    public void setPhase_text(String phase_text) {
        this.phase_text = phase_text;
    }

    public void setCurclass(String curclass) {
        this.curclass = curclass;
    }

    public void setPayment_date(String payment_date) {
        this.payment_date = payment_date;
    }

    public void setPayment_money(String payment_money) {
        this.payment_money = payment_money;
    }

    public void setPayment_allmoney(String payment_allmoney) {
        this.payment_allmoney = payment_allmoney;
    }
}
