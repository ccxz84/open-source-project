package com.example.gunmunity.model.discharge;

public class DischargeResponse {
    String dischargeDate;
    String enlistmentDate;
    String militaryAffiliate;
    String militaryStatus;

    public String getDischargeDate() {
        return dischargeDate;
    }

    public String getEnlistmentDate() {
        return enlistmentDate;
    }

    public String getMilitaryAffiliate() {
        return militaryAffiliate;
    }

    public String getMilitaryStatus() {
        return militaryStatus;
    }
}
